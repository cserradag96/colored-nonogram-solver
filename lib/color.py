class Color:
    index = 0

    def __init__(self, name, value):
        self.name = name.strip()
        self.value = value.strip()
        self.index = self.__class__.index

        self.__class__.index += 1

    def __str__(self):
        return "Color " + str(self.index) + ": " + str(self.name) + " " + str(self.value)

